-- ----------------------------------------------------------
-- 9.1 Busca por Proximidade (Raio em metros)
-- Exemplo de uso: RPC no Supabase chamando 'get_nearby_businesses'
-- ----------------------------------------------------------
create or replace function get_nearby_businesses(
  lat float8,
  lng float8,
  radius_meters int
)
returns setof get_feed
language sql
stable
as $$
  select gf.*
  from get_feed gf
  join businesses b on b.id = gf.id
  where b.location is not null
    and ST_DWithin(
          ST_SetSRID(ST_MakePoint(lng, lat), 4326)::geography,
          b.location,
          radius_meters
        )
  order by gf.is_premium desc, gf.rating desc;
$$;

-- ----------------------------------------------------------
-- 9.2 Função auxiliar: total de cliques por período
-- ----------------------------------------------------------
create or replace function get_click_stats(
  since timestamptz default now() - interval '30 days'
)
returns table (
  click_type text,
  total_clicks bigint
)
language sql
stable
as $$
  select ac.click_type::text, count(*)::bigint as total_clicks
  from analytics_clicks ac
  where ac.created_at >= since
  group by ac.click_type
  order by total_clicks desc;
$$;

