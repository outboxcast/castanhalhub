-- ============================================================
-- 8. VIEW DE FEED (Ranking)
-- ============================================================
-- Ordena primeiro os Premium e depois pela melhor nota
drop view if exists get_feed cascade;
create view get_feed as
select
  b.id,
  b.name,
  b.description,
  b.is_premium,
  b.rating,
  b.whatsapp_number,
  b.instagram_url,
  b.image_url,
  b.latitude,
  b.longitude,
  b.category_id,
  c.name as category_name,
  c.icon_name as category_icon
from businesses b
left join categories c on b.category_id = c.id
order by b.is_premium desc, b.rating desc;

