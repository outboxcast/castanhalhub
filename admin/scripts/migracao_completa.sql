-- ----------------------------------------------------------
-- 3.1 Categorias
-- ----------------------------------------------------------
-- NOTA: categories.id é UUID para consistência com businesses.id
-- e compatibilidade com o tipo String no Dart (Supabase Flutter)
create table if not exists categories (
  id UUID default gen_random_uuid() primary key,
  name TEXT not null unique,
  icon_name TEXT not null, -- Nome do ícone (ex: 'restaurant', 'shopping_bag')
  created_at TIMESTAMPTZ default NOW()
);

-- ----------------------------------------------------------
-- 3.2 Negócios (Businesses)
-- ----------------------------------------------------------
create table if not exists businesses (
  id UUID default gen_random_uuid() primary key,
  category_id UUID references categories(id) on delete set null,
  name TEXT not null,
  description TEXT,
  address TEXT,
  whatsapp_number TEXT,
  instagram_url TEXT,
  image_url TEXT, -- antigo street_view_image_url
  is_premium BOOLEAN default false,
  rating DECIMAL(3, 2) default 0 check (rating >= 0 and rating <= 5),

  -- Geolocalização
  latitude FLOAT8,
  longitude FLOAT8,

  -- Coluna calculada para PostGIS (facilita buscas por raio de distância)
  -- Nota: será NULL se latitude ou longitude forem NULL
  location GEOGRAPHY(POINT, 4326) GENERATED ALWAYS as (
    case
      when latitude is not null and longitude is not null
        then ST_SetSRID(ST_MakePoint(longitude, latitude), 4326)::GEOGRAPHY
      else null
    end
  ) STORED,

  created_at TIMESTAMPTZ default NOW(),
  updated_at TIMESTAMPTZ default NOW()
);

-- ----------------------------------------------------------
-- 3.3 Analytics
-- ----------------------------------------------------------
create table if not exists analytics_clicks (
  id BIGINT generated by default as identity primary key,
  business_id UUID references businesses(id) on delete cascade,
  click_type click_event_type not null,
  created_at TIMESTAMPTZ default NOW()
);

-- ============================================================
-- 4. MIGRAÇÃO DE COLUNA: street_view_image_url -> image_url
-- ============================================================
-- Se a tabela já existe com o nome antigo, renomeia
do $$ begin
  if exists (
    select 1 from information_schema.columns
    where table_name = 'businesses' and column_name = 'street_view_image_url'
  ) then
    alter table businesses rename column street_view_image_url to image_url;
  end if;
end $$;

-- Se a coluna image_url não existir de jeito nenhum (tabela antiga sem a coluna), cria
do $$ begin
  if not exists (
    select 1 from information_schema.columns
    where table_name = 'businesses' and column_name = 'image_url'
  ) then
    alter table businesses add column image_url text;
  end if;
end $$;

-- ============================================================
-- 5. TRIGGER: updated_at
-- ============================================================
create or replace function set_updated_at()
returns trigger as $$
begin
  NEW.updated_at = NOW();
  return NEW;
end;
$$ language plpgsql;

drop trigger if exists trg_businesses_updated_at on businesses;
create trigger trg_businesses_updated_at
  before update on businesses
  for each row
  execute function set_updated_at();

